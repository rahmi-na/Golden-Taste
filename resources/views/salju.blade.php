            <h3>Kue Putri Salju Original</h3>
            <img src="img\product\salju.jpg" class="gambar" alt="">

            <h5>Bahan-bahan :</h5>
            <ul>
                <li>250 Gram tepung terigu protein rendah (misal Kunci Biru)</li>
                <li>250 Gam mentega (butter)</li>
                <li>200 Gram kacang mede (panggang dan haluskan)</li>
                <li>100 Gram gula halus</li>
            </ul>
            <hr>
            <h5>Bahan taburan :</h5>
            <ul>
                <li>150 Gram gula bubuk</li>
                <li>1/2 Sendok teh vanili bubuk</li>
                <li>Gula donat secukupnya</li>
            </ul>
            <hr>
            <h5>Cara membuat kue putri salju :</h5>
            <ul>
                <li>1. Kocok mentega dan gula halus dengan mixer hingga lembut.</li>
                <li>2. Matikan mixer, tambahkan kacang mede dan tepung terigu, kemudian aduk rata dengan spatula.</li>
                <li>3. Bulatkan adonan, simpan di dalam kulkas selama setengah jam, kemudian bentuk bulan sabit.</li>
                <li>4. Susun putri salju di loyang yang sudah diolesi margarin atau dialasi kertas roti. Panggang putri salju di dalam oven kurang lebih 20 menit.</li>
                <li>5. Gulingkan putri salju ke gula bubuk biasa yang dicampur dengan vanili. Setelah terbalut rata, gulingkan ke gula donat.</li>
            </ul>
            <h6 class="ket">Okeyy, selamat mencoba dan berkreasi sendiri dengan Resep kue putri salju yang enakk banget!</h6>
