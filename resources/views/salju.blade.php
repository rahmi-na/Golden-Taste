            <h3>Kue Putri Salju Keju</h3>
            <br>
            <h5>Bahan-bahan :</h5>
                <ul>
                    <li>400 Gram tepung terigu protein sedang</li>
                    <li>300 Gram butter (boleh diganti Blue Band Cake & Cookies)</li>
                    <li>150 Gram keju cheddar parut</li>
                    <li>120 Gram gula halus</li>
                    <li>100 Gram tepung maizena</li>
                    <li>40 Gram susu bubuk</li>
                    <li>3 Butir kuning telur</li>
                </ul>
            <hr>
            <h5>Taburan : </h5>
            <li>Gula donat secukupnya</li>
            <hr>
            <h5>Cara membuat kue putri salju keju :</h5>
            <ul>
                <li>1. Kocok mentega, gula halus, dan kuning telur dengan mixer berkecepatan rendah selama 2 menit.</li>
                <li>2. Matikan mixer, kemudian masukkan keju parut dan aduk rata.</li>
                <li>3. Masukkan campuran terigu, tepung maizena, dan susu bubuk. Aduk rata dengan spatula sampai adonan cukup bisa diuleni.</li>
                <li>4. Cetak adonan sesuai selera, kemudian susun di loyang yang sudah diolesi margarin atau dialasi kertas roti.</li>
                <li>5. Panggang putri salju dengan suhu 150 derajat Celcius hingga matang, kurang lebih 20 menit.</li>
                <li>6. Gulingkan putri salju pada gula donat secukupnya. Setelah dingin masukkan ke dalam toples.</li>
            </ul>
