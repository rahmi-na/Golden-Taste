            <h3 class="judulResep">Resep Rahasia Ayam Bakar Taliwang Khas Lombok NTB</h3>
            <img src="img\featured\ayam.jpg" class="gambar" alt="">

            <h5>Bahan Bumbu Ayam Taliwang :</h5>
            <ul>
                <li> Ayam kampung 1 ekor (700 gram), belah dua tidak putus (jika bosan ayam, bisa diganti daging bebek dan ikan)</li>
                <li> Air jeruk limau 1 1/2 sendok teh</li>
                <li> Garam 1 sendok teh</li>
                <li> Gula merah sisir 1 sendok teh</li>
                <li> Minyak 3 sendok makan (digunakan untuk menumis)</li>
            </ul>
            <hr>
            <h5>Bumbu Halus Taliwang :</h5>
            <ul>
                <li> Bawang merah 10 butir</li>
                <li> Bawang putih 7 siung</li>
                <li> Cabai merah besar 2 buah, dibakar</li>
                <li> Cabai merah keriting 7 buah, dibakar</li>
                <li> Kemiri 5 butir, disangrai</li>
                <li> Terasi 1 1/2 sendok teh, disangrai</li>
            </ul>
            <hr>
            <h5>Rahasia Mengolah Ayam Bakar Taliwang</h5>
            <ul>
                <li>1. Lumuri ayam dengan air jeruk limau dan garam. Diamkan kurang lebih selama 20 menit. Bakar di atas bara api sambil dibolak-balik sampai setengah matang. Sisihkan.</li>
                <li>2. Panaskan minyak, tumis bumbu halus sampai tercium aroma wangi sedap.</li>
                <li>3. Kemudian masukkan santan, garam, dan gula merah sisir. Aduk rata, dan masak sampai mendidih.</li>
                <li>4. Masukkan ayam, lalu masak terus di atas api kecil sampai meresap dan matang sempurna (daging empuk) hingga bumbu menyusut.</li>
                <li>5. Terakhir bakar ayam di atas bara api sambil di olesi sisa bumbu ayam taliwang. Jangan lupa bolak-balik sampai tercium aroma harum.</li>
                <li>6. Angkat, sajikan selagi hangat dengan nasi pulen, sambal dan lalaban. Mantap!</li>
            </ul>
            <h6 class="ket">Okeyy, selamat mencoba dan berkreasi sendiri dengan Resep Rahasia Ayam Bakar Taliwang Khas LOMBOK NTB yang enakk banget!</h6>
           
